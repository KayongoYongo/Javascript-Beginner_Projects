let pi = 3.14159;
// use an inbuilt function called toFixed
let formattedNum = pi.toFixed(2);
/*  The toFixed(2) method converts the number to a string,
    rounding the number to the nearest integer and including exactly two digits after the decimal point. 
    If necessary, it adds trailing zeros to achieve the desired decimal precision.
*/
console.log(formattedNum);